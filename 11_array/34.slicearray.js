// slicing & Combining Arrays
let arr = [1, 2, 3, 4, 5];
console.log(arr.slice(1, 4)); //slices from index 1 to 3
console.log(arr.slice(2, 5)); //slices from index 2 to 4
console.log(arr.slice(-2)); //slices last 2 elements
console.log(arr.slice(0)); //copies entire array
console.log(arr.slice(2)); //slices from index 2 to end
console.log(arr.slice(2, -1)); //slices from index 2 to second last element