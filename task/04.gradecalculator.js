// check student grade calculator
let marks = 77;
if (marks >= 90) {
    console.log("Grade: A");
} else if (marks >= 80 && marks < 89) {
    console.log("Grade: B");
} else if (marks >= 70 && marks < 79) {
    console.log("Grade: C");
} else if (marks >= 60 && marks < 69) {
    console.log("Grade: D");
} else {
    console.log("Grade: Student is failed");
}